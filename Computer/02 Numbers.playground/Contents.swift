import UIKit

/*:
 # Number Literals
 
 ## Syntax
 ```
 var number = 123
 ```
 */


/*:
 # Number Types
 
 ## Syntax
 ```
 var minNumber = UInt8.min
 var maxNumber = UInt8.max
 ```
 */



/*:
 # Floating-Point Numbers
 
 ## Syntax
 ```
 var pi:Float
 var pie:Double
 ```
 */



/*:
 # Numeric Literals
 
 ## Syntax
 ```
 var decimalInteger = 17
 var binaryInteger = 0b10001       // 17 in binary notation
 var octalInteger = 0o21           // 17 in octal notation
 var hexadecimalInteger = 0x11     // 17 in hexadecimal notation
 ```
 */



/*:
 # Numeric Type Conversion
 
 ## Syntax
 ```
 var twoThousand: UInt16 = 2_000
 var one: UInt8 = 1
 var twoThousandAndOne = twoThousand + UInt16(one)
 ```
 */



/*:
 # Nil
 
 ## Syntax
 ```
 var null: Int?
 ```
